


package java_basic;

public class SwapNumberWithoutThirdVariable {

	public static void main(String[] args) {
		
		
		int a=10;
		int b=12;
		
		
		a= a+b;
		b= a-b;
		a = a-b;
		
		System.out.println("value of a"+"="+ a);
		System.out.println("value of b"+"="+ b);
		
		
	}
	
	
	
}
